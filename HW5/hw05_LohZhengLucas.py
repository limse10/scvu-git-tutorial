def make_withdraw(balance, password):
    """Return a password-protected withdraw function.

    >>> w = make_withdraw(100, 'hax0r')
    >>> w(25, 'hax0r')
    75
    >>> error = w(90, 'hax0r')
    >>> error
    'Insufficient funds'
    >>> error = w(25, 'hwat')
    >>> error
    'Incorrect password'
    >>> new_bal = w(25, 'hax0r')
    >>> new_bal
    50
    >>> w(75, 'a')
    'Incorrect password'
    >>> w(10, 'hax0r')
    40
    >>> w(20, 'n00b')
    'Incorrect password'
    >>> w(10, 'hax0r')
    "Your account is locked. Attempts: ['hwat', 'a', 'n00b']"
    >>> w(10, 'l33t')
    "Your account is locked. Attempts: ['hwat', 'a', 'n00b']"
    >>> type(w(10, 'l33t')) == str
    True
    """
    tries = 0
    attempts = []
    def w(amount, password):
        nonlocal tries, attempts, balance
        #check tries: if 3 or more, lock account
        if tries <3:
            #check password: if not correct pswd, add tries and save attempt
            if password != 'hax0r':
                tries += 1
                attempts.append(password)
                return 'Incorrect password'
            else:
                if amount > balance:
                #check if amount is insufficient
                    return 'Insufficient funds'
                else:
                    balance = balance - amount
                    return balance
        else:
            return f'Your account is locked. Attempts: {attempts}'
    return w


