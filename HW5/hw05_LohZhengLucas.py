def make_counter():
    """Return a counter function.

    >>> c = make_counter()
    >>> c('a')
    1
    >>> c('a')
    2
    >>> c('b')
    1
    >>> c('a')
    3
    >>> c2 = make_counter()
    >>> c2('b')
    1
    >>> c2('b')
    2
    >>> c('b') + c2('b')
    5
    """
    s = {}
    def counter(input):
        nonlocal s
        if s.get(input, 0) == 0:
            s.update({input:1})
            #use s[index]
        else:
            newcount = s.get(input)+1
            #s[input]
            s[input] = newcount
        return s.get(input)
    return counter

